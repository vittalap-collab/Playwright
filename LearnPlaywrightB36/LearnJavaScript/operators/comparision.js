
/*
        comparision operators
-> ==, ===, >, >=, <, <=, !=

-> = is for assignment not for comparision
-> == (loose equality) compares only value not compares datatypes
-> === (strict equality) compares value and datatypes both
*/

console.log(2=="2") // true

console.log(2==="2") // false (datatype differ)

console.log("Raju"=="raju") // false

console.log(3==3.5) // false

console.log(34<23) //false

console.log(5>=5) // true

console.log(4!=5)// true

console.log("4"+5-"6")

console.log("4"-5+6)

console.log("4"-5+"6")